#!/usr/bin/env python3
"""A function that concatenates two arrays."""


def cat_arrays(arr1, arr2):
    """
    Concatenates two arrays.

    Parameters:
    - arr1 (list): The first input array.
    - arr2 (list): The second input array.

    Returns:
    - list: The result of concatenating arr1 and arr2.
    """
    cat_mat = arr1 + arr2
    return cat_mat
